package com.java.practice;

public class Ex14 {

	public static void main(String[] args) {
		int a = 5;
		
		System.out.println(a);
		System.out.println(++a);
		System.out.println(a);
		System.out.println(a++);
		System.out.println(a);
		System.out.println(--a);
		System.out.println(a);
		System.out.println(a--);
		System.out.println(a);

	}

}
